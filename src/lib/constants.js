// Returns { name, emoji, tagline, current, next, badgesToNext }
export function getTier(badgeCount = 0) {
  const safe = Math.max(0, badgeCount | 0);
  const current = TIERS.find(t => safe >= t.min && safe <= t.max) || TIERS[0];
  const idx = TIERS.indexOf(current);
  const next = idx < TIERS.length - 1 ? TIERS[idx + 1] : null;
  const badgesToNext = next ? (next.min - safe) : 0;
  return {
    name:        current.name,
    emoji:       current.emoji,
    tagline:     current.tagline,
    current,
    next,
    badgesToNext,
    // Progress within current tier as 0..1 (for the progress bar)
    progress: next
      ? Math.min(1, Math.max(0, (safe - current.min) / (next.min - current.min)))
      : 1,
  };
}

// Back-compat shim — older code calls levelTitle(count). It now
// returns "<emoji> <name>" using the new ladder.
export function levelTitle(count = 0) {
  const t = getTier(count);
  return `${t.emoji} ${t.name}`;
}

